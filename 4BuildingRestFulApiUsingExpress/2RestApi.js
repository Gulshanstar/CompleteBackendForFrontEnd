// REST - is a short of representational state transfer which is introduced by PHD student.

// REST is basically a convention for building these http services. So we use simple http
// protocol principles to provide support to create read update and delete data. We refer to these
// operations all together as CRUD operations.

// for example [https://api/customer] this is the convention and customer is endpoint.

// so this is the RESTful convention, we expose our resources such as customers using a simple,
// meaningful address, and support various operations around them, such as creating 
// or updating them, using standard http methods.